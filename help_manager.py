from PyQt6.QtWidgets import QMessageBox


def get_split_help_text():
    """获取表格切分帮助文本
    
    Returns:
        str: 表格切分功能的详细帮助文本
    """
    help_text = ("表格切分功能说明：\n\n" +
                 "1. 输出目录：选择切分后文件的保存位置，默认为当前工作表名\n\n" +
                 "2. 保留行数：输入需要保留的表头行数，例如：8\n\n" +
                 "3. 拆分列：从左侧列信息列表拖动列到此处，可上下拖动调整顺序\n" +
                 "   程序会按切分列最后非空行的值进行分组切分\n\n" +
                 "4. 操作按钮：\n" +
                 "   - 删除选中列：删除当前选中的配置项\n" +
                 "   - 上移/下移：调整配置项的顺序\n\n" +
                 "5. 执行切分：点击后开始按配置进行表格切分\n" +
                 "   切分过程中可随时取消操作\n\n" +
                 "6. 进度显示：实时显示切分进度和状态\n\n" +
                 "使用提示：\n" +
                 "- 确保切分列包含有效的分组值\n" +
                 "- 保留行数要正确设置，否则可能导致表头丢失\n" +
                 "- 切分后每个分组会生成一个独立的Excel文件")
    
    return help_text


def show_split_help(parent):
    """显示表格切分帮助对话框
    
    Args:
        parent: 父窗口对象
    """
    help_text = get_split_help_text()
    
    QMessageBox.information(
        parent, "表格切分说明",
        help_text
    )


